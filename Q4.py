'''
4 - Dois veículos (um carro e um caminhão) saem respectivamente de cidades opostas pela mesma rodovia. O carro de Ribeirão Preto em direção a Franca, a uma velocidade constante de 110 km/h e o caminhão de Franca em direção a Ribeirão Preto a uma velocidade constante de 80 km/h. Quando eles se cruzarem na rodovia, qual estará mais próximo a cidade de Ribeirão Preto?

IMPORTANTE:

a) Considerar a distância de 100km entre a cidade de Ribeirão Preto <-> Franca.

b) Considerar 2 pedágios como obstáculo e que o caminhão leva 5 minutos a mais para passar em cada um deles e o carro possui tag de pedágio (Sem Parar)

c) Explique como chegou no resultado.
'''

# Resposta:
# Os dois estarão a uma mesma distância de Ribeirão Preto. Isso ocorre pois no momento em que se cruzam, a distância viajada pelo carro (que saiu de Ribeirão Preto) será a mesma distância que falta para o caminhão chegar a Ribeirão Preto. Nesse sentido, como eles certamente cruzarão no caminho, a distância de ambos até a cidade em questão deverá ser a mesma.
